public class oop13mathclass {
    public static void main(String[] args) {
        // double x =
        System.out.println(Math.abs(-9.5));
        System.out.println(Math.sqrt(25));
        System.out.println(Math.pow(2, 3));
        System.out.println(Math.abs(Math.PI));
        System.out.println(Math.log(2.0));
        System.out.println(Math.exp(1));
        System.out.println(Math.max(-9.5, 9));
        System.out.println(Math.min(-9.5, 5));
        System.out.println(Math.ceil(-9.5));
        System.out.println(Math.floor(-9.5));

    }
}
